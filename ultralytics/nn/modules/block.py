        y = self.conv2(x)
        y1 = self.conv3(y)
        y2 = self.conv4(y1)
        y3 = self.conv5(y2)
        y4 = self.conv6(y3)

        return self.conv7(torch.cat((x1, y, y1, y2, y3, y4), dim=1))


